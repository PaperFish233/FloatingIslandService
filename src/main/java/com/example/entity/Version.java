package com.example.entity;

public class Version {

    int vid;
    String vnumber;
    String vupdatetitle;
    String vcontent;
    String vapkurl;

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }

    public String getVnumber() {
        return vnumber;
    }

    public void setVnumber(String vnumber) {
        this.vnumber = vnumber;
    }

    public String getVupdatetitle() {
        return vupdatetitle;
    }

    public void setVupdatetitle(String vupdatetitle) {
        this.vupdatetitle = vupdatetitle;
    }

    public String getVcontent() {
        return vcontent;
    }

    public void setVcontent(String vcontent) {
        this.vcontent = vcontent;
    }

    public String getVapkurl() {
        return vapkurl;
    }

    public void setVapkurl(String vapkurl) {
        this.vapkurl = vapkurl;
    }
}
